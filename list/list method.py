l1 = [2,3,12,21,.8,9,6,4,34]
print(l1)

l1.sort()         # sorts the list
l1.reverse()       # reverse the list
l1.append(28)     # add 28 at the end of the list
l1.insert(1,123)   # insert 123 at index 1
l1.pop(1)          # remove element at index 1
l1.remove(8)        # remove 8 from list
print(l1)

